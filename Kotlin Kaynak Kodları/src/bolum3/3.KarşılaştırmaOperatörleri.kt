package bolum3

fun main(args: Array<String>) {
    var a:Int = 13
    var b:Int = 7

    var result:Boolean

    result = a < b
    println("$a < $b : "+ result)

    result = a > b
    println("$a > $b : "+ result)

    result = a <= b
    println("$a < $b : "+ result)

    result = a >= b
    println("$a < $b : "+ result)

    result = a == b
    println("$a == $b : "+ result)

    result = a != b
    println("$a != $b : "+ result)
}
